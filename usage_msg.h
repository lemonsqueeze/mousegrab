#define USAGE_MSG "\
Usage:  mousegrab [options]\n\
\n\
Lock mouse to initial window and hide it. Right mouse button to get it\n\
back. Mouse will be hidden but buttons and mouse wheel still work, and app\n\
can receive keystrokes.\n\
\n\
Useful if you have an optical mouse that jitters when off the floor and\n\
you'd rather not have it wander all over the place, or you need to click on\n\
something without worrying about mouse being moved (slides, demo ...)\n\
Especially nice to use with easystroke -> start with mouse gesture !\n\
\n\
Options:\n\
  -display <display>   specify the display to use.\n\
  --version		 show mousegrab version.\n\
"
